def get_planet_telugu(planet: str) -> str:
    return PLANETS_TELUGU.get(planet, planet)

def get_planet_short(planet: str) -> str:
    return PLANETS_TELUGU_SHORT.get(planet, planet[:2])

def get_sign_telugu(sign: str) -> str:
    return SIGNS_TELUGU.get(sign, sign)

def get_nakshatra_telugu(nakshatra: str) -> str:
    return NAKSHATRAS_TELUGU.get(nakshatra, nakshatra)

def get_house_telugu(house_num: int) -> str:
    return HOUSES_TELUGU.get(house_num, f"భావం {house_num}")

def get_day_telugu(day: str) -> str:
    return DAYS_TELUGU.get(day, day)

def get_ui_label(key: str) -> str:
    return UI_LABELS.get(key, key)
